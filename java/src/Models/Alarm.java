package Models;

public class Alarm {
	private int alertID;
	private String time;
	
	public int getAlarmID() {
		return alertID;
	}
	public void setAlarmID(int alertID) {
		this.alertID = alertID;
	}

    public int getAlertID() {
        return alertID;
    }

    public void setAlertID(int alertID) {
        this.alertID = alertID;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
